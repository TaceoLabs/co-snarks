fn install_tracing() {
    use tracing_subscriber::prelude::*;
    use tracing_subscriber::{
        EnvFilter,
        fmt::{self, format::FmtSpan},
    };

    let fmt_layer = fmt::layer()
        .with_target(false)
        .with_line_number(false)
        .with_span_events(FmtSpan::CLOSE | FmtSpan::ENTER);
    let filter_layer = EnvFilter::try_from_default_env()
        .or_else(|_| EnvFilter::try_new("info"))
        .unwrap();

    tracing_subscriber::registry()
        .with(filter_layer)
        .with(fmt_layer)
        .init();
}

#[derive(Debug, Deserialize)]
struct BenchNetworkConfig {
    network: NetworkConfig,
}

fn main() -> color_eyre::Result<ExitCode> {
    install_tracing();
    rustls::crypto::aws_lc_rs::default_provider()
        .install_default()
        .map_err(|_| eyre!("Could not install default rustls crypto provider"))?;

    // Skip the binary name and any bare `--` separator, then take the first real argument.
    let config_path = env::args().skip(1).find(|a| a != "--").ok_or_else(|| {
        eyre!("Missing config path argument. Usage: co-circom-bench [--] <configs/partyX.toml>")
    })?;
    let config_path = PathBuf::from(config_path);
    let examples_root = PathBuf::from("co-circom/co-circom/examples");
    let config_path = if config_path.exists() {
        config_path
    } else {
        examples_root.join(config_path)
    };

    let my_id = Figment::new()
        .merge(Toml::file(&config_path))
        .extract::<BenchNetworkConfig>()
        .with_context(|| {
            format!(
                "while parsing network config from {}",
                config_path.display()
            )
        })?
        .network
        .my_id;

    let groth16_vectors_root = examples_root.join("groth16/test_vectors");
    let benchmark_sizes = [10usize, 100usize];
    let curves = [(Curve::BN254, "bn254"), (Curve::BLS12_381, "bls12_381")];

    for size in benchmark_sizes {
        let case_dir = groth16_vectors_root.join(format!("BatcherOddEvenMergeSortTest{size}"));
        let canonical_circuit = case_dir.join(format!("BatcherOddEvenMergeSortTest{size}.circom"));
        let fallback_circuit = case_dir.join("circuit.circom");
        let circuit = if canonical_circuit.exists() {
            canonical_circuit
        } else if fallback_circuit.exists() {
            fallback_circuit
        } else {
            eyre::bail!(
                "Missing circuit file for size {size}: expected {} or {}",
                case_dir
                    .join(format!("BatcherOddEvenMergeSortTest{size}.circom"))
                    .display(),
                case_dir.join("circuit.circom").display()
            )
        };

        for (curve, curve_tag) in curves {
            tracing::info!("Running batcher benchmark: size={size}, curve={curve}");
            let compiler = CompilerConfig {
                simplification: SimplificationLevel::O2(usize::MAX),
                ..CompilerConfig::default()
            };

            let input_candidates = [
                case_dir.join(format!("input.{curve_tag}.json.{my_id}.shared")),
                case_dir.join(format!("input.{curve_tag}.{my_id}.shared")),
                case_dir.join(format!("input.json.{curve_tag}.{my_id}.shared")),
                case_dir.join(format!("input.json.{my_id}.shared")),
            ];
            let input_share = input_candidates
                .iter()
                .find(|p| p.exists())
                .cloned()
                .ok_or_else(|| {
                    eyre!(
                        "Missing input share for size {size}, curve {curve}, party {my_id}. Looked for: {}",
                        input_candidates
                            .iter()
                            .map(|p| p.display().to_string())
                            .collect::<Vec<_>>()
                            .join(", ")
                    )
                })?;

            let zkey_subdirs: &[&str] = match curve {
                Curve::BN254 => &["bn254"],
                Curve::BLS12_381 => &["bls", "bls12", "bls12_381"],
            };

            let artifact_dir = zkey_subdirs
                .iter()
                .map(|subdir| case_dir.join(subdir))
                .find(|dir| {
                    dir.join("circuit_final.zkey").exists()
                        && dir.join("verification_key.json").exists()
                })
                .ok_or_else(|| {
                    eyre!(
                        "Missing zkey/vkey for size {size}, curve {curve}; looked in {} under {}",
                        zkey_subdirs.join(", "),
                        case_dir.display()
                    )
                })?;

            let zkey = artifact_dir.join("circuit_final.zkey");
            let vk = artifact_dir.join("verification_key.json");

            let network = Figment::new()
                .merge(Toml::file(&config_path))
                .extract::<BenchNetworkConfig>()
                .with_context(|| {
                    format!(
                        "while parsing network config from {}",
                        config_path.display()
                    )
                })?
                .network;

            let witness_out = case_dir.join(format!("witness.{curve_tag}.{my_id}.wtns.shared"));
            let generate_witness_cfg = GenerateWitnessConfig {
                input: input_share,
                circuit: circuit.clone(),
                protocol: MPCProtocol::REP3,
                curve,
                out: witness_out.clone(),
                compiler: compiler.clone(),
                vm: VMConfig::default(),
                network,
            };

            let generate_witness_code = match curve {
                Curve::BN254 => run_generate_witness::<Bn254>(generate_witness_cfg)?,
                Curve::BLS12_381 => run_generate_witness::<Bls12_381>(generate_witness_cfg)?,
            };
            if generate_witness_code != ExitCode::SUCCESS {
                eyre::bail!("generate-witness failed for size {size}, curve {curve}")
            }

            let network = Figment::new()
                .merge(Toml::file(&config_path))
                .extract::<BenchNetworkConfig>()
                .with_context(|| {
                    format!(
                        "while parsing network config from {}",
                        config_path.display()
                    )
                })?
                .network;

            let proof_out = case_dir.join(format!("proof.{curve_tag}.{my_id}.json"));
            let public_input_out = case_dir.join(format!("public_input.{curve_tag}.{my_id}.json"));
            let generate_proof_cfg = GenerateProofConfig {
                proof_system: ProofSystem::Groth16,
                witness: witness_out,
                zkey,
                check_zkey: false,
                protocol: MPCProtocol::REP3,
                curve,
                out: Some(proof_out.clone()),
                public_input: Some(public_input_out.clone()),
                threshold: 1,
                network,
            };

            let generate_proof_code = match curve {
                Curve::BN254 => run_generate_proof::<Bn254>(generate_proof_cfg)?,
                Curve::BLS12_381 => run_generate_proof::<Bls12_381>(generate_proof_cfg)?,
            };
            if generate_proof_code != ExitCode::SUCCESS {
                eyre::bail!("generate-proof failed for size {size}, curve {curve}")
            }

            let verify_cfg = VerifyConfig {
                proof_system: ProofSystem::Groth16,
                proof: proof_out,
                curve,
                vk,
                public_input: public_input_out,
            };

            let verify_code = match curve {
                Curve::BN254 => run_verify::<Bn254>(verify_cfg)?,
                Curve::BLS12_381 => run_verify::<Bls12_381>(verify_cfg)?,
            };
            if verify_code != ExitCode::SUCCESS {
                eyre::bail!("verify failed for size {size}, curve {curve}")
            }
        }
    }

    Ok(ExitCode::SUCCESS)
}

#[instrument(level = "debug", skip(config))]
fn run_generate_witness<P: Pairing + CircomArkworksPairingBridge>(
    config: GenerateWitnessConfig,
) -> color_eyre::Result<ExitCode> {
    let input = config.input;
    let circuit = config.circuit;
    let protocol = config.protocol;
    let out = config.out;

    if protocol != MPCProtocol::REP3 {
        eyre::bail!("Only REP3 protocol is supported for witness generation");
    }

    // connect to network
    let [net0, net1] =
        TcpNetwork::networks::<2>(config.network).context("while connecting to network")?;

    // parse input shares
    let input_share_file =
        BufReader::new(File::open(&input).context("while opening input share file")?);
    let input_share: Rep3SharedInput<P::ScalarField> =
        serde_json::from_reader(input_share_file).context("trying to parse input share file")?;

    // parse circuit file & put through our compiler
    let circuit = CoCircomCompiler::<P>::parse(circuit, config.compiler)
        .context("while parsing circuit file")?;

    // Extend the witness
    tracing::info!("Starting witness generation...");
    let start = Instant::now();
    let result_witness_share =
        co_circom::generate_witness_rep3(&circuit, input_share, config.vm, &net0, &net1)?;
    let duration_ms = start.elapsed().as_micros() as f64 / 1000.;
    tracing::info!("Generate witness took {duration_ms} ms");

    // write result to output file
    let out_file = BufWriter::new(std::fs::File::create(&out)?);
    bincode::serialize_into(
        out_file,
        &CompressedRep3SharedWitness::from(result_witness_share),
    )?;
    tracing::info!("Witness successfully written to {}", out.display());
    Ok(ExitCode::SUCCESS)
}

#[instrument(level = "debug", skip(config))]
fn run_generate_proof<P: Pairing + CircomArkworksPairingBridge>(
    config: GenerateProofConfig,
) -> color_eyre::Result<ExitCode> {
    let proof_system = config.proof_system;
    let witness = config.witness;
    let zkey = config.zkey;
    let protocol = config.protocol;
    let out = config.out;
    let public_input_filename = config.public_input;
    let t = config.threshold;
    let n = config.network.parties.len();
    let check = if config.check_zkey {
        CheckElement::Yes
    } else {
        CheckElement::No
    };

    // parse witness shares
    let witness_file =
        BufReader::new(File::open(witness).context("trying to open witness share file")?);

    // parse Circom zkey file
    let zkey_file = File::open(zkey)?;

    tracing::info!("Starting proof generation...");
    let public_input = match proof_system {
        ProofSystem::Groth16 => {
            let [net0, net1] =
                TcpNetwork::networks::<2>(config.network).context("while connecting to network")?;

            let zkey = Groth16ZKey::<P>::from_reader(zkey_file, check).context("reading zkey")?;
            let (matrices, pkey) = zkey.into();

            let (proof, public_input) = match protocol {
                MPCProtocol::REP3 => {
                    if t != 1 {
                        eyre::bail!("REP3 only allows the threshold to be 1");
                    }

                    let witness_share: CompressedRep3SharedWitness<P::ScalarField> =
                        bincode::deserialize_from(witness_file)?;
                    let witness_share = co_circom::uncompress_shared_witness(witness_share, &net0)?;
                    let public_input = witness_share.public_inputs.clone();

                    let start = Instant::now();
                    let proof = Rep3CoGroth16::prove::<_, CircomReduction>(
                        &net0,
                        &net1,
                        &pkey,
                        &matrices,
                        witness_share,
                    )?;
                    let duration_ms = start.elapsed().as_micros() as f64 / 1000.;
                    tracing::info!("Generate proof took {duration_ms} ms");

                    (proof, public_input)
                }
                MPCProtocol::SHAMIR => {
                    let witness_share: ShamirSharedWitness<P::ScalarField> =
                        bincode::deserialize_from(witness_file)?;
                    let public_input = witness_share.public_inputs.clone();

                    let start = Instant::now();
                    let proof = ShamirCoGroth16::prove::<_, CircomReduction>(
                        &net0,
                        &net1,
                        n,
                        t,
                        &pkey,
                        &matrices,
                        witness_share,
                    )?;
                    let duration_ms = start.elapsed().as_micros() as f64 / 1000.;
                    tracing::info!("Generate proof took {duration_ms} ms");

                    (proof, public_input)
                }
            };

            // write result to output file
            if let Some(out) = out {
                // convert proof to Groth16Proof for serialization
                let proof = CircomGroth16Proof::from(proof);

                let out_file = BufWriter::new(
                    std::fs::File::create(&out).context("while creating output file")?,
                );

                serde_json::to_writer(out_file, &proof)
                    .context("while serializing proof to JSON file")?;
                tracing::info!("Wrote proof to file {}", out.display());
            }
            public_input
        }
        ProofSystem::Plonk => {
            let nets =
                TcpNetwork::networks::<8>(config.network).context("while connecting to network")?;
            let zkey = Arc::new(
                PlonkZKey::<P>::from_reader(zkey_file, check).context("while parsing zkey")?,
            );

            let (proof, public_input) = match protocol {
                MPCProtocol::REP3 => {
                    if t != 1 {
                        eyre::bail!("REP3 only allows the threshold to be 1");
                    }

                    let witness_share: CompressedRep3SharedWitness<P::ScalarField> =
                        bincode::deserialize_from(witness_file)?;
                    let witness_share =
                        co_circom::uncompress_shared_witness(witness_share, &nets[0])?;
                    let public_input = witness_share.public_inputs.clone();

                    let start = Instant::now();
                    let proof = Rep3CoPlonk::prove(&nets, zkey, witness_share)?;
                    let duration_ms = start.elapsed().as_micros() as f64 / 1000.;
                    tracing::info!("Generate proof took {duration_ms} ms");
                    (proof, public_input)
                }
                MPCProtocol::SHAMIR => {
                    let witness_share: ShamirSharedWitness<P::ScalarField> =
                        bincode::deserialize_from(witness_file)?;
                    let public_input = witness_share.public_inputs.clone();

                    let start = Instant::now();
                    let proof = ShamirCoPlonk::prove(&nets, n, t, zkey, witness_share)?;
                    let duration_ms = start.elapsed().as_micros() as f64 / 1000.;
                    tracing::info!("Generate proof took {duration_ms} ms");
                    (proof, public_input)
                }
            };

            // write result to output file
            if let Some(out) = out {
                let out_file = BufWriter::new(
                    std::fs::File::create(&out).context("while creating output file")?,
                );

                serde_json::to_writer(out_file, &proof)
                    .context("while serializing proof to JSON file")?;
                tracing::info!("Wrote proof to file {}", out.display());
            }
            public_input
        }
    };

    // write public input to output file
    if let Some(public_input_filename) = public_input_filename {
        let public_input_as_strings = public_input
            .iter()
            .skip(1) // we skip the constant 1 at position 0
            .map(|f| {
                if f.is_zero() {
                    "0".to_string()
                } else {
                    f.to_string()
                }
            })
            .collect::<Vec<String>>();
        let public_input_file = BufWriter::new(
            std::fs::File::create(&public_input_filename)
                .context("while creating public input file")?,
        );
        serde_json::to_writer(public_input_file, &public_input_as_strings)
            .context("while writing out public inputs to JSON file")?;
        tracing::info!(
            "Wrote public inputs to file {}",
            public_input_filename.display()
        );
    }
    tracing::info!("Proof generation finished successfully");
    Ok(ExitCode::SUCCESS)
}

#[instrument(level = "debug", skip(config))]
fn run_verify<P: Pairing + CircomArkworksPairingBridge>(
    config: VerifyConfig,
) -> color_eyre::Result<ExitCode> {
    let proofsystem = config.proof_system;
    let proof = config.proof;
    let vk = config.vk;
    let public_input = config.public_input;

    // parse circom proof file
    let proof_file = BufReader::new(File::open(&proof).context("while opening proof file")?);

    // parse circom verification key file
    let vk_file = BufReader::new(File::open(&vk).context("while opening verification key file")?);

    // parse public inputs
    let public_inputs_file =
        BufReader::new(File::open(&public_input).context("while opening public inputs file")?);
    let public_inputs_as_strings: Vec<String> = serde_json::from_reader(public_inputs_file)
        .context(
            "while parsing public inputs, expect them to be array of stringified field elements",
        )?;
    // skip 1 atm
    let public_inputs = public_inputs_as_strings
        .into_iter()
        .map(|s| {
            s.parse::<P::ScalarField>()
                .map_err(|_| eyre!("could not parse as field element: {}", s))
        })
        .collect::<Result<Vec<P::ScalarField>, _>>()
        .context("while converting public input strings to field elements")?;

    // verify proof
    tracing::info!("Starting proof verification...");
    let start = Instant::now();
    let res = match proofsystem {
        ProofSystem::Groth16 => {
            let proof: CircomGroth16Proof<P> = serde_json::from_reader(proof_file)
                .context("while deserializing proof from file")?;

            let vk: Groth16JsonVerificationKey<P> = serde_json::from_reader(vk_file)
                .context("while deserializing verification key from file")?;

            // convert circom vk and proof types to arkworks types
            let vk = vk.into();
            let proof = proof.into();

            Groth16::<P>::verify(&vk, &proof, &public_inputs)
        }
        ProofSystem::Plonk => {
            let proof: PlonkProof<P> = serde_json::from_reader(proof_file)
                .context("while deserializing proof from file")?;

            let vk: PlonkJsonVerificationKey<P> = serde_json::from_reader(vk_file)
                .context("while deserializing verification key from file")?;

            Plonk::<P>::verify(&vk, &proof, &public_inputs)
        }
    };
    let duration_ms = start.elapsed().as_micros() as f64 / 1000.;
    tracing::info!("Verify took {} ms", duration_ms);

    match res {
        Ok(_) => {
            tracing::info!("Proof verified successfully");
            Ok(ExitCode::SUCCESS)
        }
        Err(VerificationError::InvalidProof) => {
            tracing::error!("Proof verification failed");
            Ok(ExitCode::FAILURE)
        }
        Err(VerificationError::Malformed(err)) => eyre::bail!(err),
    }
}
